<h2>Development site</h2>

Development is coordinated via an
<a href="http://gforge.inria.fr/projects/mpc">INRIA Gforge project</a>.

In particular, we offer
<a href="http://gforge.inria.fr/scm/?group_id=131">anonymous
subversion access</a>
and access to the
<a href="http://gforge.inria.fr/tracker/?atid=607&group_id=131&func=browse">bug</a>
and
<a href="http://gforge.inria.fr/tracker/?atid=610&group_id=131&func=browse">feature
request</a>
trackers.

The
<a href="http://lists.gforge.inria.fr/mailman/listinfo/mpc-discuss">mailing
list</a> for user and developer discussions is hosted there as well.
